namespace Enemies
{
    /// <summary> Defines a zombie. </summary>
    public class Zombie
    {
        /// <summary> The zombie's health. </summary>
        public int health;

        /// <summary>
        /// Initializes a new instance of the <see cref="Zombie"/> class.
        /// Sets the health of the zombie to 0.
        /// </summary>
        public Zombie()
        {
            health = 0;
        }
    }
}